"""Head-to-head benchmark harness — honestly measure bad-research against a
commercial Deep Research tool (Gemini / OpenAI / Perplexity / Grok DR) on a
shared query set, scored by the EXISTING categorical (E2) judge.

Honesty boundary (this is the apparatus, NOT a claimed win):
  - Competitor RUNS are MANUAL. No product is called from here; a human pastes
    the competitor's report as a file. This module never proves a win — it makes
    a win (or a loss) measurable and reproducible.
  - Scoring reuses `calibrate.judge` verbatim: the keyless deterministic
    `RubricJudge` by default ($0, offline), or the host-model `LLMJudge` on the
    opt-in `--llm` path. The RubricJudge's documented scope limits (golden.py:130)
    apply here too: it is a grounding-overlap + citation-presence proxy, NOT
    semantic entailment. Read the scorecard's `disclaimer` before quoting it.
  - BLINDING: every entrant report is stripped of tool-identifying markers before
    it reaches the judge, so the categorical verdict cannot be biased by which
    tool produced the text. Blinding is lexical and best-effort — it removes the
    markers we know about; see `TOOL_MARKERS`.

The protocol that frames this lives in `docs/benchmarks/HEAD_TO_HEAD.md`; the
shared starter query set in `docs/benchmarks/queries/starter_set.json`.
"""

